# CONVERSORES A BOOLEANOS

# convertir el real 1.5 a boleano
base=1.5
a=bool(base)
print(a,type(a))

# convertir  la division 4//5 entera a boleano
altura=4//5
a=bool(altura)
print(a,type(a))

# convertir  la multiplicacion 0.3*3 real a boleano
fuerza=0.3*3
a=bool(fuerza)
print(a,type(a))

# convertir el real 31%3 a boleano
masa=31%3
a=bool(masa)
print(a,type(a))

# convertir el entero 30%5 a boleano
velocidad=30%5
a=bool(velocidad)
print(a,type(a))

# convertir  el real 0.4444444444 a boleano
espacio=0.44444444444
a=bool(espacio)
print(a,type(a))

# convertir el entero 1 a boleano
talla=1
a=bool(talla)
print(a,type(a))

# convertir la cadena "juan" a boleano
peso=len("juan")
a=bool(peso)
print(a,type(a))

# convertir la cadena "" a boleano
calza=len("")
a=bool(calza)
print(a,type(a))

# convertir la cadena " " a boleano
densidad=" "
